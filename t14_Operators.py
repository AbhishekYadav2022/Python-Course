# Operators in python 

## Arithmetic Operators 
print("5 + 5 =", 5+5)
print("5 - 5 =", 5-5)
print("5 * 5 =", 5*5)
print("5 / 5 =", 5/5)
print("5 // 5 =", 5//5) # Important # It returns interger value
print("5 % 5 =", 5%5)

## Assignment Operators 
